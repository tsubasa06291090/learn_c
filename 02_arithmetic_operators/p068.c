// 前置増分と後置増分
#include <stdio.h>

int main()
{
    int a[] = {10, 11, 12, 13};
    int b[] = {10, 11, 12, 13};
    int i;

    printf("0 1 2 3\n");
    i = 0;
    a[++i] = 0; // 代入される前に+する
    a[++i] = 0;
    a[++i] = 0;
    printf("a[] %2d %2d %2d %2d\n", a[0], a[1], a[2], a[3]);
    i = 0;
    b[i++] = 0; // 代入されてから+する
    b[i++] = 0;
    b[i++] = 0;
    printf("b[] %2d %2d %2d %2d\n", b[0], b[1], b[2], b[3]);
}